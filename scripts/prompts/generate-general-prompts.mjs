function makePrompt(profile, scenario, index, stamp) {
  const variables = parseVars(scenario.vars);
  return {
    id: `${V2_PREFIX}${profile.modalitySlug}-${profile.slug}-${scenario.slug}-${pad(index + 1)}`,
    title: `${profile.tool} · ${scenario.title}`,
    category: profile.category,
    tags: unique([
      `工具:${profile.tool}`,
      `模态:${profile.modality}`,
      `用途:${scenario.title}`,
      index % 3 === 0 ? "难度:入门" : index % 3 === 1 ? "难度:中级" : "难度:进阶",
      profile.official,
      "v2原创模板",
      profile.modality
    ]),
    variables,
    status: "推荐",
    summary: `${profile.tool} 专用的“${scenario.title}”模板：${scenario.scene}，包含复制即用提示词、设置建议、迭代方法、失败修正和安全边界。`,
    body: bodyFor(profile, scenario, index),
    source: SOURCE,
    createdAt: stamp,
    updatedAt: stamp,
    archived: false
  };
}

async function main() {
  if (process.argv.includes("--help") || process.argv.includes("-h")) {
    console.log("Usage: node scripts/prompts/generate-general-prompts.mjs");
    console.log("Replaces old general-template prompts with 1800 v2 tool-oriented original templates.");
    return;
  }

  const generated = [];
  for (const profile of profiles) {
    for (let index = 0; index < profile.count; index += 1) {
      const scenario = scenarioFor(profile, index);
      if (!scenario) throw new Error(`Missing scenario ${index} for ${profile.tool}`);
      generated.push(makePrompt(profile, scenario, index, timestamp(new Date())));
    }
  }

  if (generated.length !== 1800) {
    throw new Error(`Expected 1800 v2 prompts, got ${generated.length}`);
  }

  const raw = JSON.parse(await readFile(libraryPath, "utf8"));
  const existing = Array.isArray(raw.prompts)
    ? raw.prompts.filter((item) => {
        const id = String(item?.id || "");
        return !id.startsWith(OLD_PREFIX) && !id.startsWith(V2_PREFIX);
      })
    : [];
  const stamp = timestamp(new Date());

  raw.version = 2;
  raw.updatedAt = stamp;
  raw.prompts = [...existing, ...generated.map((item) => ({ ...item, createdAt: stamp, updatedAt: stamp }))];
  raw.assets = Array.isArray(raw.assets) ? raw.assets : [];

  await writeFile(libraryPath, `${JSON.stringify(raw, null, 2)}\n`, "utf8");
  console.log(`Kept existing prompts: ${existing.length}`);
  console.log(`Generated v2 prompts: ${generated.length}`);
  console.log(`Total prompts: ${raw.prompts.length}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
