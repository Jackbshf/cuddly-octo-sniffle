function profileGroup(modalitySlug, modality, expectedProfiles, perProfile, rows) {
  if (rows.length !== expectedProfiles) {
    throw new Error(`Expected ${expectedProfiles} ${modalitySlug} profiles, got ${rows.length}`);
  }
  return rows.map(([slug, tool, category, strength, settings, official]) => ({
    modalitySlug,
    modality,
    slug,
    tool,
    category,
    count: perProfile,
    strength,
    settings,
    official
  }));
}

function pad(value, size = 2) {
  return String(value).padStart(size, "0");
}

function timestamp(date) {
  return `${date.getFullYear()}-${pad(date.getMonth() + 1)}-${pad(date.getDate())} ${pad(date.getHours())}:${pad(date.getMinutes())}:${pad(date.getSeconds())}`;
}

function parseVars(text) {
  const base = unique(text.split("、").map((item) => item.trim()).filter(Boolean).map((item) => `{${item}}`));
  const fallbacks = ["{目标平台}", "{输出格式}", "{限制条件}", "{质量标准}", "{参考素材}"];
  for (const item of fallbacks) {
    if (base.length >= 5) break;
    if (!base.includes(item)) base.push(item);
  }
  return base;
}

function unique(values) {
  return [...new Set(values.filter(Boolean))];
}

